<template>
  <div class="register">
    <el-form ref="registerRef" :model="registerForm" :rules="registerRules" class="register-form">
      <h3 class="title">{{ title }}</h3>
      
      <el-tabs v-model="activeRole" stretch style="margin-bottom: 20px;">
          <el-tab-pane label="考生/家长注册" name="student"></el-tab-pane>
          <el-tab-pane label="高校入驻申请" name="college"></el-tab-pane>
      </el-tabs>

      <el-form-item prop="username">
        <el-input 
          v-model="registerForm.username" 
          type="text" 
          size="large" 
          auto-complete="off" 
          placeholder="账号"
        >
          <template #prefix><svg-icon icon-class="user" class="el-input__icon input-icon" /></template>
        </el-input>
      </el-form-item>
      
      <!-- 考生专属字段 -->
      <template v-if="activeRole === 'student'">
          <el-form-item prop="candidateId">
            <el-input v-model="registerForm.candidateId" size="large" placeholder="考生号 (18位)" >
               <template #prefix><el-icon class="el-input__icon input-icon"><CreditCard /></el-icon></template>
            </el-input>
          </el-form-item>
           <el-form-item prop="province">
            <el-select v-model="registerForm.province" placeholder="高考省份" size="large" style="width: 100%">
                 <el-option label="北京" value="北京"></el-option>
                 <el-option label="四川" value="四川"></el-option>
            </el-select>
          </el-form-item>
      </template>
      
       <!-- 高校专属字段 -->
      <template v-if="activeRole === 'college'">
          <el-form-item prop="collegeCode">
            <el-input v-model="registerForm.collegeCode" size="large" placeholder="院校代码 (5位)" >
               <template #prefix><el-icon class="el-input__icon input-icon"><School /></el-icon></template>
            </el-input>
          </el-form-item>
           <el-form-item prop="contactPerson">
             <el-input v-model="registerForm.contactPerson" size="large" placeholder="联系人姓名" >
               <template #prefix><el-icon class="el-input__icon input-icon"><UserFilled /></el-icon></template>
            </el-input>
          </el-form-item>
      </template>

      <el-form-item prop="password">
        <el-input
          v-model="registerForm.password"
          type="password"
          size="large" 
          auto-complete="off"
          placeholder="密码"
          @keyup.enter="handleRegister"
        >
          <template #prefix><svg-icon icon-class="password" class="el-input__icon input-icon" /></template>
        </el-input>
      </el-form-item>
      <el-form-item prop="confirmPassword">
        <el-input
          v-model="registerForm.confirmPassword"
          type="password"
          size="large" 
          auto-complete="off"
          placeholder="确认密码"
          @keyup.enter="handleRegister"
        >
          <template #prefix><svg-icon icon-class="password" class="el-input__icon input-icon" /></template>
        </el-input>
      </el-form-item>
      <el-form-item prop="code" v-if="captchaEnabled">
        <el-input
          size="large" 
          v-model="registerForm.code"
          auto-complete="off"
          placeholder="验证码"
          style="width: 63%"
          @keyup.enter="handleRegister"
        >
          <template #prefix><svg-icon icon-class="validCode" class="el-input__icon input-icon" /></template>
        </el-input>
        <div class="register-code">
          <img :src="codeUrl" @click="getCode" class="register-code-img"/>
        </div>
      </el-form-item>
      <el-form-item style="width:100%;">
        <el-button
          :loading="loading"
          size="large" 
          type="primary"
          style="width:100%;"
          @click.prevent="handleRegister"
        >
          <span v-if="!loading">注 册</span>
          <span v-else>注 册 中...</span>
        </el-button>
        <div style="float: right;">
          <router-link class="link-type" :to="'/login'">使用已有账户登录</router-link>
        </div>
      </el-form-item>
    </el-form>
    <!--  底部  -->
    <div class="el-register-footer">
      <span>{{ footerContent }}</span>
    </div>
  </div>
</template>

<script setup>
import { ElMessageBox } from "element-plus"
import { getCodeImg, register } from "@/api/login"
import defaultSettings from '@/settings'

const title = import.meta.env.VITE_APP_TITLE
const footerContent = defaultSettings.footerContent
const router = useRouter()
const { proxy } = getCurrentInstance()

const activeRole = ref('student')

const registerForm = ref({
  username: "",
  password: "",
  confirmPassword: "",
  code: "",
  uuid: "",
  candidateId: "",
  province: "",
  collegeCode: "",
  contactPerson: ""
})

const equalToPassword = (rule, value, callback) => {
  if (registerForm.value.password !== value) {
    callback(new Error("两次输入的密码不一致"))
  } else {
    callback()
  }
}

const registerRules = {
  username: [
    { required: true, trigger: "blur", message: "请输入您的账号" },
    { min: 2, max: 20, message: "用户账号长度必须介于 2 和 20 之间", trigger: "blur" }
  ],
  password: [
    { required: true, trigger: "blur", message: "请输入您的密码" },
    { min: 5, max: 20, message: "用户密码长度必须介于 5 和 20 之间", trigger: "blur" },
    { pattern: /^[^<>"'|\\]+$/, message: "不能包含非法字符：< > \" ' \\\ |", trigger: "blur" }
  ],
  confirmPassword: [
    { required: true, trigger: "blur", message: "请再次输入您的密码" },
    { required: true, validator: equalToPassword, trigger: "blur" }
  ],
  code: [{ required: true, trigger: "change", message: "请输入验证码" }]
}

const codeUrl = ref("")
const loading = ref(false)
const captchaEnabled = ref(true)

function handleRegister() {
  proxy.$refs.registerRef.validate(valid => {
    if (valid) {
      loading.value = true
      // Pass activeRole to backend in real scenario
      register({ ...registerForm.value, role: activeRole.value }).then(res => {
        const username = registerForm.value.username
        let msg = "恭喜你，您的账号 " + username + " 注册成功！"
        if(activeRole.value === 'college') {
            msg = "高校入驻申请提交成功，请等待管理员审核！"
        }
        ElMessageBox.alert("<font color='red'>"+ msg +"</font>", "系统提示", {
          dangerouslyUseHTMLString: true,
          type: "success",
        }).then(() => {
          router.push("/login")
        }).catch(() => {})
      }).catch(() => {
        loading.value = false
        if (captchaEnabled) {
          getCode()
        }
      })
    }
  })
}

function getCode() {
  getCodeImg().then(res => {
    captchaEnabled.value = res.captchaEnabled === undefined ? true : res.captchaEnabled
    if (captchaEnabled.value) {
      codeUrl.value = "data:image/gif;base64," + res.img
      registerForm.value.uuid = res.uuid
    }
  })
}

getCode()
</script>

